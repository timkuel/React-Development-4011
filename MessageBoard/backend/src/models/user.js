const mongoose = require('mongoose');

const UserPassSchema = new mongoose.Schema({
  firstname: {
    type: String,
    required: true
  },
  lastname: {
    type: String,
    required: true
  },
  username: {
    type: String,
    required: true,
    unique: true
  },
  password: {
    type: String,
    required: true
  },
  dateJoined: {
    type: String,
    required: false
  }
});

module.exports = mongoose.model('userpasswords', UserPassSchema);
